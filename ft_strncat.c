#include "libft.h"
#include <string.h>

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	unsigned int i;
	unsigned int len;

	i = 0;
	len = ft_strlen(dest);
	if (dest == NULL || src == NULL)
		return (NULL);
	while (i < n && src[i])
	{
		dest[len + i] = src[i];
		i++;
	}
	dest[len + i] = '\0';
	return (dest);
}
